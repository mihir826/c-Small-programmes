#include <iostream>
using namespace std;

// 123.456 and 76.543

int main() {

    double x = 123.456;
    double y = 76.543;
    double sum = x + y;
    double difference = x - y;
    cout << "Sum of x + y =" << sum << endl;
    cout << "Difference of x - y =" << difference << endl;
    return 0;
}

